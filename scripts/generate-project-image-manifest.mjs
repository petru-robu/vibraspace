import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, "..");
const projectsDir = path.join(repoRoot, "public", "projects");
const outputFile = path.join(repoRoot, "src", "data", "project_image_manifest.js");
const imageExtensions = new Set([".gif", ".webp"]);
const filenameCollator = new Intl.Collator(undefined, { numeric: true, sensitivity: "base" });

const compareNames = (a, b) => filenameCollator.compare(a.name, b.name);

async function directoryExists(directory) {
  try {
    const stats = await fs.stat(directory);
    return stats.isDirectory();
  } catch (error) {
    if (error.code === "ENOENT") return false;
    throw error;
  }
}

async function collectImagePaths(directory, relativeSegments = []) {
  if (!(await directoryExists(directory))) return [];

  const entries = await fs.readdir(directory, { withFileTypes: true });
  entries.sort(compareNames);

  const images = [];

  for (const entry of entries) {
    const entryPath = path.join(directory, entry.name);
    const entrySegments = [...relativeSegments, entry.name];

    if (entry.isDirectory()) {
      images.push(...await collectImagePaths(entryPath, entrySegments));
      continue;
    }

    if (imageExtensions.has(path.extname(entry.name).toLowerCase())) {
      images.push(entrySegments);
    }
  }

  return images;
}

function toPublicUrl(segments) {
  return `/${segments.map(segment => encodeURIComponent(segment)).join("/")}`;
}

async function buildManifest() {
  if (!(await directoryExists(projectsDir))) return {};

  const projectEntries = await fs.readdir(projectsDir, { withFileTypes: true });
  projectEntries.sort(compareNames);

  const manifest = {};

  for (const entry of projectEntries) {
    if (!entry.isDirectory()) continue;

    const imgDir = path.join(projectsDir, entry.name, "img");
    if (!(await directoryExists(imgDir))) continue;

    const key = `/projects/${entry.name}/img`;
    const images = await collectImagePaths(imgDir);
    manifest[key] = images.map(imagePath => toPublicUrl(["projects", entry.name, "img", ...imagePath]));
  }

  return manifest;
}

function formatManifest(manifest) {
  const lines = [
    "// This file is generated by scripts/generate-project-image-manifest.mjs.",
    "// Do not edit by hand; add/remove files under public/projects/{id}/img instead.",
    "",
    "const projectImageManifest = {",
  ];

  for (const [folder, images] of Object.entries(manifest)) {
    lines.push(`  ${JSON.stringify(folder)}: [`);

    for (const image of images) {
      lines.push(`    ${JSON.stringify(image)},`);
    }

    lines.push("  ],");
  }

  lines.push("};", "", "export default projectImageManifest;", "");

  return lines.join("\n");
}

const manifest = await buildManifest();
await fs.writeFile(outputFile, formatManifest(manifest));
